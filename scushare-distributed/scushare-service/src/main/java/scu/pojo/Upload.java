	/**
	 * @return the upId
	 */
	public int getUpId() {
		return upId;
	}

	/**
	 * @param upId the upId to set
	 */
	public void setUpId(int upId) {
		this.upId = upId;
	}

	/**
	 * @return the userId
	 */
	public int getUserId() {
		return userId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(int userId) {
		this.userId = userId;
	}

