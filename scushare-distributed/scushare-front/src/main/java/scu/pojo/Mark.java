package scu.pojo;

import java.sql.Date;

public class Mark {
	
	/**
	 * @return the makeId
	 */
	public int getMakeId() {
		return makeId;
	}

	/**
	 * @param makeId the makeId to set
	 */
	public void setMakeId(int makeId) {
		this.makeId = makeId;
	}

	/**
	 * @return the userId
	 */
	public int getUserId() {
		return userId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(int userId) {
		this.userId = userId;
	}

	/**
	 * @return the fileId
	 */
	public int getFileId() {
		return fileId;
	}

	/**
	 * @param fileId the fileId to set
	 */
	public void setFileId(int fileId) {
		this.fileId = fileId;
	}

	/**
	 * @return the markTime
	 */
	public Date getMarkTime() {
		return markTime;
	}

	/**
	 * @param markTime the markTime to set
	 */
	public void setMarkTime(Date markTime) {
		this.markTime = markTime;
	}

	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}

	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}

	/**
	 * @return the markGrade
	 */
	public int getMarkGrade() {
		return markGrade;
	}

	/**
	 * @param markGrade the markGrade to set
	 */
	public void setMarkGrade(int markGrade) {
		this.markGrade = markGrade;
	}

	private int makeId;
	
	private int userId;
	
	private int fileId;
	
	private Date markTime;
	
	private String userName;
	
	private int markGrade;

}
